package com.java4.popcorn.mycalendar;

public class MyCalendarVO {
    private int calendarlogNo;
    private String movieId;
    private String member_id;
    private int year;
    private int month;
    private int day;
    private String movieTitle;
    
	public int getCalendarlogNo() {
		return calendarlogNo;
	}
	public void setCalendarlogNo(int calendarlogNo) {
		this.calendarlogNo = calendarlogNo;
	}
	public String getMovieId() {
		return movieId;
	}
	public void setMovieId(String movieId) {
		this.movieId = movieId;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public String getMovieTitle() {
		return movieTitle;
	}
	public void setMovieTitle(String movieTitle) {
		this.movieTitle = movieTitle;
	}
	
}